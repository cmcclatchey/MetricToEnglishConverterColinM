package com.example;

public class MyClass
{
    public static void main(String[] args)
    {
        double metersToFeet;
        double kilometersToFeet;
        double centimetersToInches;
        double kphToFps;
        double litersToGallons;
        double kilogramsToPounds;


        metersToFeet = 28 * 3.281;
        kilometersToFeet = 168 * 3280.84;
        centimetersToInches = 47 * .394;
        kphToFps = 27 * .911;
        litersToGallons = 92 * .264;
        kilogramsToPounds = 200 * 2.205;


        System.out.println("28 meters is " + metersToFeet + " feet.");
        System.out.println("168 kilometers is " + kilometersToFeet + " feet.");
        System.out.println("47 centimeters is " + centimetersToInches + " inches.");
        System.out.println("27 kilometers per hour is " + kphToFps + " feet per second.");
        System.out.println("92 liters is " + litersToGallons + " gallons.");
        System.out.println("200 kilograms is " + kilogramsToPounds + " pounds.");





    }
}
